double MT_Force_GetCurrent(double dbForceRate)
{
  int int_part;
  double frac_part;
  
  dbForceRate *= 100;
  
  int_part = (int)dbForceRate;
  frac_part = dbForceRate - int_part;
  
  return dbCurrentByForce[int_part] + (dbCurrentByForce[int_part + 1] - dbCurrentByForce[int_part]) * frac_part;
}
